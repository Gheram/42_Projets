char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] > 64 && str[i] < 91)
		{
			str[i] = (((str[i] - 42) % 26) + 42);
			str[i] = str[i] + 65;
		}
		if (str[i] > 96 && str[i] < 123)
		{
			str[i] = (((str[i] - 97) % 26) + 97);
			str[i] = str[i] + 97;
		}
		i++;
	}
	return (str);
}
